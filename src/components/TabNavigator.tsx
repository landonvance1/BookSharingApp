import React from 'react';
import { createBottomTabNavigator } from '@react-navigation/bottom-tabs';
import { Ionicons } from '@expo/vector-icons';

import { BookSearchPage } from '../features/books/components/BookSearchPage';
import { LibraryStack } from '../features/library/LibraryStack';
import { SharesStack } from '../features/shares/SharesStack';
import CommunitiesScreen from '../features/communities/CommunitiesScreen';
import SettingsScreen from '../features/settings/SettingsScreen';
import { useShareUnreadCount } from '../features/notifications/hooks/useNotifications';

const Tab = createBottomTabNavigator();

export default function TabNavigator() {
  const unreadCount = useShareUnreadCount();

  return (
    <Tab.Navigator
        screenOptions={({ route }) => ({
          tabBarIcon: ({ focused, color, size }) => {
            let iconName: any;

            switch (route.name) {
              case 'Search':
                iconName = focused ? 'search' : 'search-outline';
                break;
              case 'Library':
                iconName = focused ? 'library' : 'library-outline';
                break;
              case 'Shares':
                iconName = focused ? 'gift' : 'gift-outline';
                break;
              case 'Communities':
                iconName = focused ? 'people' : 'people-outline';
                break;
              case 'Settings':
                iconName = focused ? 'settings' : 'settings-outline';
                break;
              default:
                iconName = 'circle';
            }

            return <Ionicons name={iconName} size={size} color={color} />;
          },
          tabBarActiveTintColor: '#007AFF',
          tabBarInactiveTintColor: 'gray',
          headerShown: false,
        })}
      >
        <Tab.Screen name="Search" component={BookSearchPage} />
        <Tab.Screen name="Library" component={LibraryStack} />
        <Tab.Screen
          name="Shares"
          component={SharesStack}
          options={{
            tabBarBadge: unreadCount > 0 ? unreadCount : undefined,
          }}
        />
        <Tab.Screen name="Communities" component={CommunitiesScreen} />
        <Tab.Screen name="Settings" component={SettingsScreen} />
      </Tab.Navigator>
  );
}